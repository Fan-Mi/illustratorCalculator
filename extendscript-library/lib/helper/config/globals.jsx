/**
 * Module with globals variables
 * @namespace Globals
 * @memberOf H
 * @author Bastien Eichenberger
 */
H.Globals = (function(my) {

    /**
     * log_level the severity log level, by default set to debug
     * @public
     * @memberOf H.Globals
     * @type {Number}
     */
    my.log_level = 0;



    return my;
})(H.Globals || {} );
